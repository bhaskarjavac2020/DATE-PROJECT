package com.bk.Employe;

public class Employee {
	private Long id;
	private String name;
	private Double slary;
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Double getSlary() {
		return slary;
	}
	public void setSlary(Double slary) {
		this.slary = slary;
	}
}
